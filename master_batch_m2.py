models = [
    {
        "name": "m2_qwen1_8b",
        "model_id": "Qwen/Qwen1.5-1.8B-Chat",
        "results_dir": "m2_Qwen_1_8B",
        "ablated_dir": "m2_qwen1_8b_ablated",
        "layer": 15,
        "scripts": scripts_all + scripts_controls
    }
]

config_path = r"C:\Users\USER\Desktop\Urja\Q1_final\cognitive_dissociation\scripts\config.py"

def write_config(m):
    with open(config_path, "w", encoding="utf-8") as f:
        f.write(f'''import os

# --- DYNAMIC CONFIG GENERATED BY MASTER BATCH M2 ---
CURRENT_RUN = "{m['name']}"
MODEL_ID = "{m['model_id']}"
RESULTS_DIR_NAME = "{m['results_dir']}"
ABLATED_MODEL_DIR_NAME = "{m['ablated_dir']}"
DEFAULT_LAYER = {m['layer']}

# Helper paths
BASE_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
RESULTS_DIR = os.path.join(BASE_DIR, RESULTS_DIR_NAME)

def get_results_dir():
    os.makedirs(RESULTS_DIR, exist_ok=True)
    return RESULTS_DIR

def get_ablated_model_dir():
    return os.path.join(BASE_DIR, "models", ABLATED_MODEL_DIR_NAME)
''')

base_wd = r"C:\Users\USER\Desktop\Urja\Q1_final\cognitive_dissociation"

for m in models:
    print(f"\n{'='*50}\nStarting hybrid pipeline for {m['name']}\n{'='*50}")
    write_config(m)
    for script in m["scripts"]:
        script_path = os.path.join(base_wd, "scripts", script)
        print(f"[{m['name']}] Running {script}...")
        try:
            subprocess.run(["python", script_path], cwd=base_wd, check=True)
        except subprocess.CalledProcessError as e:
            print(f"ERROR running {script} for {m['name']}. Continuing...")
            pass

print("\nM2 HYBRID BATCH EXECUTION COMPLETE!")
